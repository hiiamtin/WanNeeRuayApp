package com.wanneeruay.wanneeruay.Firebase;

public class Spacecraft {
    String key;
    String value;

    public Spacecraft() {
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    @Override
    public String toString() {
        return value;
    }
}
